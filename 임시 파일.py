def func2(N, t_type=0): # 디폴트 인자, 입력이 안되도 기본으로 이값을 지정
    
    if t_type ==0:
        t_type = range(1,10)
    elif t_type == 1:
        t_type = range(1,10)
        t_type = sorted(t_type, reverse = True)  # 역정렬
    
    else :
        print("Syntax Error!")
        # 각 조건문마다 출력변수를 선언 저장 해서
        # 반복문을 조건 만족시에 반복하게 설정해두자
        # output= 1, output= 0 등등
        
        
    for gd in t_type:
        print("%3d * %3d = %3d" %(N, gd, N*gd))

func2(5,1)
